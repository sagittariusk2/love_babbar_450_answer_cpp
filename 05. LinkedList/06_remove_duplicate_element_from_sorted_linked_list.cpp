//Function to remove duplicates from sorted linked list.
Node *removeDuplicates(Node *head) {
    Node* first=head, *last=head->next;
    while(last) {
        while(last and last->data==first->data) {
            last = last->next;
        }
        first->next=last;
        if(last) {
            last = last->next;
            first = first->next;
        }
    }
    return head;
}