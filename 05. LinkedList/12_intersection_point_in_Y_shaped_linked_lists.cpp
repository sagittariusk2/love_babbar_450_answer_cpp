//Function to find intersection point in Y shaped Linked Lists.

int length(Node* head) {
    if(head) {
        return 1+length(head->next);
    }
    return 0;
}


int intersectPoint(Node* head1, Node* head2) {
    int l1 = length(head1);
    int l2 = length(head2);
    int d = abs(l1-l2);
    
    Node *p = head1, *q = head2;
    if(l1>l2) {
        while(d--) {
            p=p->next;
        }
    } else {
        while(d--) {
            q = q->next;
        }
    }
    
    while(p!=q) {
        p = p->next;
        q = q->next;
    }
    return p->data;
}

